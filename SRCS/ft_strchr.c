#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	a;

	a = 0;
	while (s[a] != '\0')
	{
		if (s[a] == (char)c)
		{
			return ((char *)(s + a));
			break ;
		}
		a++;
	}
	if ((char)c == '\0' && s[a] == '\0')
		return ((char *)(s + a));
	return (NULL);
}
